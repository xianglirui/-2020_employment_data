<template>
  <div id="app">
    <router-view/>
  </div>
</template>

<script>

export default {
  name: 'App',
  components: {

  },
  mounted() {
    if (!this._isMobile()) {
      this.$message.warning('这是电脑端')
      this.$router.push('/pc_err');
    }else {
      if(window.location.pathname==='/pc_err'){
        this.$router.push('/cover')
      }
    }
  },
  methods:{
    _isMobile() {
      return navigator.userAgent.match(/(phone|pad|pod|iPhone|iPod|ios|iPad|Android|Mobile|BlackBerry|IEMobile|MQQBrowser|JUC|Fennec|wOSBrowser|BrowserNG|WebOS|Symbian|Windows Phone)/i);
    }
  }
}
</script>

